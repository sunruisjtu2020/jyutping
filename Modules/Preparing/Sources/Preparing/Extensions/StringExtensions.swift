extension String {

        /// Empty String. `String.init()`
        static let empty: String = String.init()

        /// U+0009. Horizontal tab. `\t`
        static let tab: String = "\t"

        /// U+000A. `\n`
        static let newLine: String = "\n"

        /// U+0020
        static let space: String = "\u{20}"

        /// U+0027 ( ' ) apostrophe
        static let separator: String = "\u{27}"
}
